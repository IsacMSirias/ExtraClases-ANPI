function pregunta_4()
  clc; clear;close all
  warning('off','all')
  f=@(x) cos(x) .* exp(x);
  a=2;
  b=5;
  k=7;
  n=20;
  g=@(x)((b-a)/2)*f(((b-a)*x+(b+a))/2);

  I_S= cuad_gauss(g,a,b,k);
  I_C = cuad_gauss_comp(f,a,b,k,n);
  I_T = cuad_gauss_iter(f,a,b,k);
  fprintf('Simple: %g\n', I_S);
  fprintf('Compuesta: %g\n', I_C);
  fprintf('Iterativa: %g\n', I_T);

end


function I = cuad_gauss(f,a,b,k)
  [x,w]=nodos_pesos(k);
  I=0;
  for i=1:k
    I=I+w(i)*f(x(i));
  endfor
endfunction

function I = cuad_gauss_comp(f,a,b,k,n)
  h=(b-a)/n;
  x=a:h:b;
  %nodos y pesos estándar en [-1,1]
  [xi, wi] = nodos_pesos(k);

  I=0;

  for j =1:n
    aj = x(j);
    bj = x(j+1);

    % cambio de variable local en cada subintervalo
    g = @(t) ((bj - aj)/2) * f( ((bj - aj)*t + (bj + aj))/2 );

    % sumar regla gaussiana en cada subintervalo
    S = 0;
    for i = 1:k
      S = S + wi(i)*g(xi(i));
    endfor

    I = I + S;
  endfor
endfunction

function I = cuad_gauss_iter(f,a,b,k)
  tol = 1e-8;
  iterMax = 1e6;
  I=0;

  % primera aproximación con n = 1
  S_prev = cuad_gauss_comp(f,a,b,k,1);

  for n = 2:iterMax
    S_curr = cuad_gauss_comp(f,a,b,k,n);
    err = abs(S_curr - S_prev);

    if err < tol
      I = S_curr;
      return
    endif

    S_prev = S_curr;
  endfor

  % si llega aquí, no convergió
  warning("No se alcanzó la tolerancia antes del máximo de iteraciones");
  I = S_prev;

endfunction

